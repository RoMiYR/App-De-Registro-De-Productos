    public void addMouseListener() {
        tblProductos.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int filaSeleccionada = tblProductos.rowAtPoint(e.getPoint());

                if (e.getButton() == MouseEvent.BUTTON1 && filaSeleccionada != -1) {

                    //Mostramos el id seleccionado
                    tfID.setText(tblProductos.getValueAt(filaSeleccionada, 0).toString());
                    tfNombre.setText(tblProductos.getValueAt(filaSeleccionada, 1).toString());
                    tfPrecio.setText(tblProductos.getValueAt(filaSeleccionada, 2).toString());
                    tfCategoria.setText(tblProductos.getValueAt(filaSeleccionada, 3).toString());

                }

            }

        });
    }

