    public void configurarTabla() {

        ///Hacemos que las celdas no sean editables.
        modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //Agregamos la columnas
        modeloTabla.addColumn("ID");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Precio");
        modeloTabla.addColumn("Categoria");

        this.tblProductos.setModel(modeloTabla);

    }

    public void cargarTabla(ArrayList<Producto> lista) {

        //Limpiamos la tabla antes de cargarla.
        modeloTabla.setRowCount(0);

        //Recorremos la lista y agregamos los productos.
        for (Producto producto : lista) {
            Object[] fila = {
                producto.getId(),
                producto.getNombre(),
                producto.getPrecio(),
                producto.getCategoria()
            };
            modeloTabla.addRow(fila);
        }

    }

